    /**
     * Detection de la pression d'une touche du clavier. Si c'est une des
     * flèches et qu'un pion est sélectionné, on essaye de déplacer ce pion dans
     * la direction de la flèche.
     *
     * @param evt
     */
    private void formKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_formKeyPressed
        Direction d = null;
        switch (evt.getKeyCode()) {
            case KeyEvent.VK_DOWN:
                d = Direction.BAS;
                break;
            case KeyEvent.VK_UP:
                d = Direction.HAUT;
                break;
            case KeyEvent.VK_LEFT:
                d = Direction.GAUCHE;
                break;
            case KeyEvent.VK_RIGHT:
                d = Direction.DROITE;
                break;
        }

        if (selected != null && d != null) {

            if (imageJeton(selected).seDeplacer(d)) {
                dejaBouge = true;
                this.deplacementsRestants.setText(String.valueOf(imageJeton(selected).getDeplacementsRestants()));
                updatePlateau();
                if(imageJeton(selected).peutFinirTour()){
                    boutonFinTour.setEnabled(true);
                }else{
                    boutonFinTour.setEnabled(false);
                }
            }
        }
    }//GEN-LAST:event_formKeyPressed

